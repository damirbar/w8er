package com.w8er.android.model;

import java.util.ArrayList;

public class MenuItemsId {

    private ArrayList<String> appetizer;
    private ArrayList<String> main_course;
    private ArrayList<String> dessert;
    private ArrayList<String> drinks;
    private ArrayList<String> deals;
    private ArrayList<String> specials;

    public ArrayList<String> getAppetizer() {
        return appetizer;
    }

    public void setAppetizer(ArrayList<String> appetizer) {
        this.appetizer = appetizer;
    }

    public ArrayList<String> getMain_course() {
        return main_course;
    }

    public void setMain_course(ArrayList<String> main_course) {
        this.main_course = main_course;
    }

    public ArrayList<String> getDessert() {
        return dessert;
    }

    public void setDessert(ArrayList<String> dessert) {
        this.dessert = dessert;
    }

    public ArrayList<String> getDrinks() {
        return drinks;
    }

    public void setDrinks(ArrayList<String> drinks) {
        this.drinks = drinks;
    }

    public ArrayList<String> getDeals() {
        return deals;
    }

    public void setDeals(ArrayList<String> deals) {
        this.deals = deals;
    }

    public ArrayList<String> getSpecials() {
        return specials;
    }

    public void setSpecials(ArrayList<String> specials) {
        this.specials = specials;
    }
}
